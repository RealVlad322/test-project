@Injectable()
export class MstucaSheduleMapperService {
  getAllMappedShedule(fullStat: MstucaResponse[]): CreateSheduleDto[] {
    const result: CreateSheduleDto[] = [];
    fullStat.forEach((sub) => {
      const shedule: CreateSheduleDto = {
        date: sub.date,
        grade: 0,
        group: 0,
        discipline: sub.discipline,
        faculty: '',
        groupName: '',
        index: 0,
        type: sub.kindOfWork,
        place: sub.auditorium,
        teacher: sub.lecturer,
        address: sub.building,
        syncedAt: new Date().toISOString(),
      };

      if (sub.subGroup) {
        const result = this._transformGroup(sub.subGroup, true);
        const { grade, group, groupName, subgroup } = result;

        shedule.groupName = groupName;
        shedule.grade = grade;
        shedule.subgroup = subgroup;
        shedule.group = group;
      } else if (sub.parentschedule) {
        const result = this._transformGroup(
          (sub.parentschedule.includes('.')
            ? sub.parentschedule.split('.')[0]
            : sub.parentschedule.includes(',')
              ? sub.parentschedule.split(',')[0]
              : sub.parentschedule).trim(),
        );
        const { grade, group, groupName } = result;

        shedule.groupName = groupName;
        shedule.grade = grade;
        shedule.group = group;
      } else {
        const result = this._transformGroup(sub.stream);
        const { grade, group, groupName } = result;

        shedule.groupName = groupName;
        shedule.grade = grade;
        shedule.group = group;
      }

      if (sub.duration > 2) {
        shedule.index = sub.lessonNumberStart;
        result.push({ ...shedule });
      }

      shedule.index = sub.lessonNumberEnd;

      result.push({ ...shedule });
    });

    return result;
  }

  private _transformGroup(
    name: string,
    subgroup?: boolean,
  ): {
      groupName: string;
      grade: number;
      group: number;
      subgroup?: number;
    } {
    const temp = (TRANSLATED_GROUP_NAME[name] as string)?.split('-');

    if (subgroup) {
      const groupName = temp?.at(-3) ?? '';
      const grade = temp?.at(-3)?.at(-1) ?? 0;
      const group = temp?.at(-2) ?? 0;
      const subgroup = temp?.at(-1) ?? 0;

      return {
        grade: +grade,
        group: +group,
        subgroup: +subgroup,
        groupName,
      };
    }

    const groupName = temp?.at(-2) ?? '';
    const grade = temp?.at(-2)?.at(-1) ?? 0;
    const group = temp?.at(-1) ?? 0;

    return {
      grade: +grade,
      group: +group,
      groupName,
    };
  }
}
